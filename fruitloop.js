//switch fruit loop by fruit
let fruit = "apple";
switch (fruit) {
    case "apple":
        console.log("this is apple");
        break;
    case "mango":
        console.log("this is mango");
        break;
    case "banana":
        console.log("this is banana");
        break;
    default:
        console.log("this is default");
        break;
}

console.log(fruit);



